-- Step 1 — Create a New Migration Run
-- Create a new run record
INSERT INTO MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_RUN (
    RUN_ID,
    RUN_NAME,
    STATUS,
    TRIGGERED_BY
)
SELECT
    UUID_STRING(),
    'POC End-to-End Verification Run 2',
    'IN_PROGRESS',
    'END_TO_END_SCRIPT';

-- Capture the new RUN_ID into a session variable
SET RUN_ID = (
    SELECT RUN_ID
    FROM MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_RUN
    ORDER BY STARTED_AT DESC
    LIMIT 1
);

SELECT $RUN_ID AS RUN_ID;

-- SELECT * FROM MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_RUN;

-- Step 2 — Run Assessment
CALL MIGRATION_ACCELERATOR_DEV.ASSESSMENT.SP_ASSESS_SCHEMA(
    $RUN_ID,
    'tickit'
);

-- Verify
SELECT SOURCE_TABLE, STATUS
FROM MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_TABLE_REGISTRY
WHERE RUN_ID = $RUN_ID
ORDER BY SOURCE_TABLE;

-- Step 3 — Run Translation
CALL MIGRATION_ACCELERATOR_DEV.TRANSLATION.SP_TRANSLATE_DDL(
    $RUN_ID
);

CALL MIGRATION_ACCELERATOR_DEV.TRANSLATION.SP_CLASSIFY_QUERIES(
    $RUN_ID
);

-- Verify
SELECT SOURCE_TABLE, STATUS
FROM MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_TABLE_REGISTRY
WHERE RUN_ID = $RUN_ID
ORDER BY SOURCE_TABLE;

-- Step 4 — Run Load
CALL MIGRATION_ACCELERATOR_DEV.LOAD.SP_BATCH_LOAD_CONTROLLER(
    $RUN_ID
);

-- Verify
SELECT SOURCE_TABLE, STATUS
FROM MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_TABLE_REGISTRY
WHERE RUN_ID = $RUN_ID
ORDER BY SOURCE_TABLE;

-- Step 5 — Run Validation
CALL MIGRATION_ACCELERATOR_DEV.VALIDATION.SP_VALIDATE_ROW_COUNTS(
    $RUN_ID
);

CALL MIGRATION_ACCELERATOR_DEV.VALIDATION.SP_VALIDATE_NULL_RATES(
    $RUN_ID,
    'USERS'
);

CALL MIGRATION_ACCELERATOR_DEV.VALIDATION.SP_VALIDATE_NULL_RATES(
    $RUN_ID,
    'SALES'
);

-- Verify
SELECT SOURCE_TABLE, STATUS
FROM MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_TABLE_REGISTRY
WHERE RUN_ID = $RUN_ID
ORDER BY SOURCE_TABLE;

--Step 6 — Final Pipeline Log Check
SELECT PHASE, STEP_NAME, STATUS, ROWS_PROCESSED, ERROR_MESSAGE
FROM MIGRATION_ACCELERATOR_DEV.CONTROL.PIPELINE_RUN_LOG
WHERE RUN_ID = $RUN_ID
ORDER BY LOGGED_AT;


-- Step 7 — Update Run Status
UPDATE MIGRATION_ACCELERATOR_DEV.CONTROL.MIGRATION_RUN
SET STATUS = 'COMPLETED'
WHERE RUN_ID = $RUN_ID;